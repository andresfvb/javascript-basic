// Iteradores en JS

const tecnologias = ['HTML', 'CSS', 'JavaScript', 'React', 'Node.js']

// forEach = accedera a cada elemento 
const arrayForeach = tecnologias.forEach(function(papa){
    return papa
})

// Map = crea un nuevo array
const arrayMap = tecnologias.map(function(papa){
    return papa
})

console.log(arrayForeach)
console.log(arrayMap)